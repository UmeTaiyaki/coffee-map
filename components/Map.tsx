function ChangeMapView({ center, zoom }: { center: [number, number], zoom: number }) {
  const map = useMap()
  useEffect(() => {
    map.setView(center, zoom)
  }, [center, zoom, map])
  return null
}

interface Shop {
  id: number
  name: string
  address: string
  description?: string
  latitude: number
  longitude: number
  created_at?: string
  phone?: string
  opening_hours?: string
  has_wifi?: boolean
  has_power?: boolean
  website?: string
}

interface ShopWithDistance extends Shop {
  distance: number
  isFavorite?: boolean
}

interface MapProps {
  refreshTrigger: number
}

